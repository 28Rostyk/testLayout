function renderTable() {
  const start = (currentPage - 1) * itemsPerPage;
  const end = start + itemsPerPage;
  const pageItems = items.slice(start, end);

  const markup = pageItems
    .map(
      ({ customerName, company, phoneNumber, email, country, isActive }) =>
        `<tr>
          <td>${customerName}</td>
          <td>${company}</td>
          <td>${phoneNumber}</td>
          <td>${email}</td>
          <td>${country}</td>
          <td><button class="table-btn" type="button">${isActive}</button></td>
        </tr>`
    )
    .join('');

  refs.table.innerHTML = markup;
}

function renderPagination() {
  let paginationMarkup = '';

  paginationMarkup += `<button class="pagination-item prev-page">
  <svg class="menu-svg-arrow" width="16" height="16">
                  <use href="./images/symbol-defs.svg#icon-chevron-right"></use>
                </svg></button>`;

  if (totalPages <= 7) {
    for (let i = 1; i <= totalPages; i += 1) {
      paginationMarkup += `<button class="pagination-item${
        i === currentPage ? ' active' : ''
      }" data-page="${i}">${i}</button>`;
    }
  } else {
    let startPage, endPage;

    if (currentPage <= 3) {
      startPage = 1;
      endPage = Math.min(4, totalPages);
    } else if (currentPage >= totalPages - 3) {
      startPage = Math.max(totalPages - 4, 1);
      endPage = totalPages;
    } else {
      startPage = currentPage - 1;
      endPage = currentPage + 2;
    }

    for (let i = startPage; i <= endPage; i += 1) {
      paginationMarkup += `<button class="pagination-item${
        i === currentPage ? ' active' : ''
      }" data-page="${i}">${i}</button>`;
    }

    if (currentPage <= totalPages - 4) {
      paginationMarkup += `<span class="pagination-dots">...</span>
                        <button class="pagination-item" data-page="${totalPages}">${totalPages}</button>`;
    }
  }

  paginationMarkup += `<button class="pagination-item next-page">
  <svg class="menu-svg-arrow" width="16" height="16">
                  <use href="./images/symbol-defs.svg#icon-chevron-right"></use>
                </svg></button>`;

  refs.paginationContainer.innerHTML = paginationMarkup;

  const prevPageButton = document.querySelector('.prev-page');
  const nextPageButton = document.querySelector('.next-page');

  prevPageButton.addEventListener('click', () => {
    if (currentPage > 1) {
      changePage(currentPage - 1);
    }
  });

  nextPageButton.addEventListener('click', () => {
    if (currentPage < totalPages) {
      changePage(currentPage + 1);
    }
  });

  const paginationItems = document.querySelectorAll(
    '.pagination-item:not(.prev-page):not(.next-page)'
  );
  paginationItems.forEach(item => {
    item.addEventListener('click', () => {
      const page = parseInt(item.dataset.page);
      changePage(page);
    });
  });
}

function changePage(page) {
  currentPage = page;
  renderTable();
  renderPagination();
}

